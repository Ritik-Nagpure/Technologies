# out ==> output statement to give answer to user
# in ==> input statement to take values from user

print("Hello there" + "!" * 3 + "\n I can predict future if you fill the below form")  # out
name = input(" your good name : \n ")  # in
age = int(input(" how old are you ?\n"))  # in
print("Dear " + name + "\nI regret to inform you that you will be " + str((age + 1)) + " years old in 1 year.")  # out
