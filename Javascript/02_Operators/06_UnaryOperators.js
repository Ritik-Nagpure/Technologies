// ++	Increment
console.log(++a); // Pre   curr val ==> 4
console.log(a++); // Post  curr val ==> 4


// --	Decrement
console.log(a--);  //Post  curr val ==> 5
console.log(--a);  //Pre   curr val ==> 4

