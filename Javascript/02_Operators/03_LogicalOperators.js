// Logical (Boolean) Operator

let var1 = 10 , var2 = 50;

// AND statement ==> both should be true
console.log(var1 > 5 && var2 < 70)

// OR statement  ==> either one should be true
console.log(var1 > 5 || var2 < 70)

// Not statement ==> 
console.log(!true)
console.log(!false)

// ?? Nullish coalescing operator
console.log((null || undefined) ?? "foo");

